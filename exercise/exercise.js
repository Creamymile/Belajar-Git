function firstMessage(msg1, msg2) {
  // msg = parameter
  return msg1 + " " + msg2; // msg + Spasi + msg
}

const msg1 = "Hello"; // string word
const msg2 = "World"; // string word
const result = firstMessage(msg1, msg2); // msg 1 for "Hello" msg 2 for "World"

console.log(result); // result = Hello World
